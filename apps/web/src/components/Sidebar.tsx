export function Sidebar() {
  const pathname = usePathname();
  const {
    user, signOut, profile, isAdmin, isSyndic,
    availableTenants, switchTenant, switchingTenant, currentTenantName,
  } = useAuthContext();

  // Itens de navegação baseados nas permissões
  const navigation = [
    { name: 'Dashboard', href: '/dashboard', icon: HomeIcon, show: true },
    { name: 'Condominios', href: '/dashboard/tenants', icon: OfficeBuildingIcon, show: isAdmin },
    { name: 'Usuarios', href: '/dashboard/users', icon: UserGroupIcon, show: isAdmin },
    { name: 'Unidades', href: '/dashboard/units', icon: BuildingIcon, show: isSyndic },
    { name: 'Moradores', href: '/dashboard/residents', icon: UsersIcon, show: isSyndic },
    { name: 'Comunicados', href: '/dashboard/announcements', icon: MegaphoneIcon, show: isSyndic },
    { name: 'Assembleias', href: '/dashboard/assemblies', icon: CalendarIcon, show: true },
    { name: 'Ocorrencias', href: '/dashboard/issues', icon: ClipboardIcon, show: true },
    { name: 'Reservas', href: '/dashboard/reservations', icon: CalendarDaysIcon, show: true },
    { name: 'Plano', href: '/dashboard/billing', icon: CreditCardIcon, show: isAdmin },
  ].filter(item => item.show);

  const getRoleBadge = () => {
    if (!profile?.role) return null;
    const labels: Record<string, string> = {
      SAAS_ADMIN: 'Admin',
      SYNDIC: 'Sindico',
      DOORMAN: 'Porteiro',
      RESIDENT: 'Morador',
    };
    return labels[profile.role] || profile.role;
  };

  return (
    <div className="flex flex-col w-64 bg-gradient-to-b from-attrio-navy to-attrio-blue h-screen shadow-attrio-lg fixed left-0 top-0">
      {/* Logo */}
      <div className="flex items-center justify-center h-24 px-6 border-b border-white/10 flex-shrink-0">
        <img
          src="/images/logo.png"
          alt="Attrio"
          className="h-16 w-auto brightness-0 invert"
          style={{ filter: 'brightness(0) invert(1)' }}
        />
      </div>

      {/* Tenant Switcher */}
      {availableTenants.length > 1 && (
        <div className="px-4 py-3 border-b border-white/10">
          <label className="block text-xs font-medium text-white/60 mb-1.5">
            Condominio
          </label>
          <select
            value={profile?.tenantId || ''}
            onChange={(e) => {
              if (e.target.value && e.target.value !== profile?.tenantId) {
                switchTenant(e.target.value);
              }
            }}
            disabled={switchingTenant}
            className="w-full px-3 py-2 text-sm bg-white/10 text-white border border-white/20 rounded-lg appearance-none cursor-pointer focus:outline-none focus:ring-2 focus:ring-white/30 disabled:opacity-50 disabled:cursor-wait"
            style={{
              backgroundImage: `url("data:image/svg+xml,%3csvg xmlns='http://www.w3.org/2000/svg' fill='none' viewBox='0 0 20 20'%3e%3cpath stroke='%23ffffff' stroke-linecap='round' stroke-linejoin='round' stroke-width='1.5' d='M6 8l4 4 4-4'/%3e%3c/svg%3e")`,
              backgroundPosition: 'right 0.5rem center',
              backgroundSize: '1.5em 1.5em',
              backgroundRepeat: 'no-repeat',
              paddingRight: '2.5rem',
            }}
          >
            {availableTenants.map((tenant) => (
              <option key={tenant.id} value={tenant.id} className="text-gray-900 bg-white">
                {tenant.name}
              </option>
            ))}
          </select>
        </div>
      )}
      {availableTenants.length === 1 && (
        <div className="px-4 py-3 border-b border-white/10">
          <p className="text-xs font-medium text-white/60">Condominio</p>
          <p className="text-sm text-white font-medium truncate mt-0.5">
            {currentTenantName}
          </p>
        </div>
      )}

      {/* Navigation */}
      <nav className="flex-1 px-4 py-6 space-y-1 overflow-y-auto">
        {navigation.map((item) => {
          // Para o Dashboard, apenas a rota exata. Para outras, inclui sub-rotas
          const isActive = item.href === '/dashboard'
            ? pathname === item.href
            : pathname === item.href || pathname.startsWith(item.href + '/');
          return (
            <Link
              key={item.name}
              href={item.href}
              className={`flex items-center px-4 py-3 text-sm font-medium rounded-lg transition-all duration-200 ${
                isActive
                  ? 'bg-white text-attrio-navy shadow-attrio-sm'
                  : 'text-white/90 hover:bg-white/10 hover:text-white'
              }`}
            >
              <item.icon className="w-5 h-5 mr-3" />
              {item.name}
            </Link>
          );
        })}
      </nav>

      {/* User section */}
      <div className="p-4 border-t border-white/10">
        <Link
          href="/dashboard/profile"
          className="flex items-center mb-3 px-2 py-2 rounded-lg text-white/80 hover:bg-white/10 hover:text-white transition-all"
        >
          <div className="flex-shrink-0">
            <div className="w-10 h-10 rounded-full bg-attrio-green flex items-center justify-center">
              <span className="text-white text-sm font-medium">
                {user?.email?.charAt(0).toUpperCase() || 'U'}
              </span>
            </div>
          </div>
          <div className="ml-3 flex-1 min-w-0">
            <p className="text-sm font-medium text-white truncate">
              {user?.email || 'Usuario'}
            </p>
            <div className="flex items-center gap-2 mt-0.5">
              {getRoleBadge() && (
                <span className="text-xs px-2 py-0.5 bg-attrio-green rounded text-white font-medium">
                  {getRoleBadge()}
                </span>
              )}
              <span className="text-xs text-white/60">Editar perfil</span>
            </div>
          </div>
        </Link>
        <button
          onClick={signOut}
          className="w-full flex items-center px-2 py-2 text-sm text-white/70 hover:text-white hover:bg-white/10 rounded-lg transition-all"
        >
          <svg className="w-4 h-4 mr-2" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 9V5.25A2.25 2.25 0 0 0 13.5 3h-6a2.25 2.25 0 0 0-2.25 2.25v13.5A2.25 2.25 0 0 0 7.5 21h6a2.25 2.25 0 0 0 2.25-2.25V15m3 0 3-3m0 0-3-3m3 3H9" />
          </svg>
          Sair
        </button>
      </div>
    </div>
  );
}
